    private void tampilDataTable() {
        String[] kolom = {"ID", "Kode", "Mata Kuliah", "Hari", "Jam Mulai", "Jam Selesai", "Ruangan", "Dosen"};
        tableModel = new DefaultTableModel(null, kolom);
        for (jdwl_mkModel model : jdwl_mkManager.tampilSemua()) {
            String[] row = new String[kolom.length];
            row[0] = String.valueOf(model.getId());
            row[1] = model.getKode();
            row[2] = model.getMataKuliah();
            row[3] = model.getHari();
            row[4] = model.getJamMulai().toString();
            row[5] = model.getJamSelesai().toString();
            row[6] = model.getRuangan();
            row[7] = model.getDosen();
                                    
            tableModel.addRow(row);
        }
        tbl_jdwlmk.setModel(tableModel);
        tbl_jdwlmk.clearSelection();
    }
    
    private void refresh(){
        tampilDataTable();
        clearData();
        sedangDiEdit = false;
    }
    
