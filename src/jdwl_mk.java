    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        // TODO add your handling code here:
        String kode = txt_kode.getText();
        String matakuliah = txt_mk.getText();
        String hari = txt_hari.getText();
        String jam_mulai = txt_jm.getText();
        String jam_selesai = txt_js.getText();
        String ruangan = txt_ruangan.getText();
        String dosen = txt_dosen.getText();

        if (!kode.isEmpty() && !matakuliah.isEmpty() && !hari.isEmpty() && !jam_mulai.isEmpty() && !jam_selesai.isEmpty()
                && !ruangan.isEmpty() && !dosen.isEmpty()) {
            if (sedangDiEdit) {
                String id = (String) tbl_jdwlmk.getValueAt(tbl_jdwlmk.getSelectedRow(), 0);
                Time JamMulai = Time.valueOf(jam_mulai);
                Time JamSelesai = Time.valueOf(jam_selesai);

                jdwl_mkManager.update(kode, matakuliah, hari, JamMulai, JamSelesai, ruangan, dosen, Integer.valueOf(id));
            } else {
                Time JamMulai = Time.valueOf(jam_mulai);
                Time JamSelesai = Time.valueOf(jam_selesai);

                jdwl_mkManager.insert(kode, matakuliah, hari, JamMulai, JamSelesai, ruangan, dosen);
            }
            tampilDataTable();
            clearData();
            sedangDiEdit = false;
        } else {
            JOptionPane.showMessageDialog(null, "Tidak boleh ada yang kosong !");
        }
    }//GEN-LAST:event_simpanMouseClicked

